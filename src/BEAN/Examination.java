package BEAN;

public class Examination {
	private int examinationid;
	private String examinationame;
	private String examinationage;
	private int checked;
	
	public int getExaminationid() {
		return examinationid;
	}
	public void setExaminationid(int examinationid) {
		this.examinationid = examinationid;
	}
	public String getExaminationame() {
		return examinationame;
	}
	public void setExaminationame(String examinationame) {
		this.examinationame = examinationame;
	}
	public String getExaminationage() {
		return examinationage;
	}
	public void setExaminationage(String examinationage) {
		this.examinationage = examinationage;
	}
	public int getChecked() {
		return checked;
	}
	public void setChecked(int checked) {
		this.checked = checked;
	}

}
